Super Wild symbol 可以代替任何一個 symbol

3 個 Super Wild 在任何地方，會觸發 "free spin"
"free spin" 基本是 10 次

在 "free spin" 中，若出現：
1. multiplier man(the Tripler)--> win * 3
2. multiplier man(the Doubler)--> win * 2
3. expando man(the Expanding)--> Wild will expand to all positions
4. spin twins (Extra Free Spins) --> 5 extra free spins awarded

